function cleanPageText(text) {
  return text
    .replace(/\r/g, '')
    .replace(/[ \t]+/g, ' ')
    .replace(/^\s*[—-]\s*\d+\s*[—-]\s*$/gm, '')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

const extraction = spawnSync('pdftotext', ['-layout', inputPath, '-'], {
  encoding: 'utf8',
  maxBuffer: 10 * 1024 * 1024,
});
if (extraction.error) throw extraction.error;
if (extraction.status !== 0) throw new Error(extraction.stderr || 'pdftotext执行失败');

const rawPages = extraction.stdout.split('\f');
if (rawPages.length < 274) {
  throw new Error(`PDF分页数量异常:${rawPages.length}`);
}

const pages = rawPages.slice(0, 274).map((rawText, index) => {
  const pdfPage = index + 1;
  const printedPage = pdfPage >= 7 ? pdfPage - 6 : null;
  const document = printedPage
    ? documents.find(item => printedPage >= item.startPage && printedPage <= item.endPage)
    : null;
  return {
    pdfPage,
    printedPage,
    section: document?.section || (pdfPage === 1 ? '前言' : '目录'),
    documentTitle: document?.title || (pdfPage === 1 ? '前言' : '目录'),
    documentStartPage: document?.startPage || null,
    text: cleanPageText(rawText),
  };
});

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
